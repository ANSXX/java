import java.util.Scanner;
public class sumof
{
    public static void main(String args[])
    {
        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
        int sum = 0;
        for(int i = sum;i<=n;i++){
            sum = sum + i;
        }
        System.out.println(sum);
        scan.close();
    }
}
